int_timer:

	;-------------------------------------
	; Save register
	;-------------------------------------
	pushad
	push	ds
	push	es

	;-------------------------------------
	; Set data segment
	;-------------------------------------
	mov	ax,	0x0010
	mov	ds,	ax
	mov	es,	ax

	;-------------------------------------
	; TICK
	;-------------------------------------
	inc	dword	[TIMER_COUNT]

	;-------------------------------------
	; Clear interrupt flag (EOI)
	;-------------------------------------
	outp	0x20,	0x20

	;-------------------------------------
	; Recover register
	;-------------------------------------
	pop	es
	pop	ds
	popad

	iret

ALIGN 4,	db	0
TIMER_COUNT:	dq	0
