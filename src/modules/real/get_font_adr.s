get_font_adr:

	;-------------------------------------
	; Build stack frame
	;-------------------------------------
	push	bp
	mov	bp,	sp

	;-------------------------------------
	; Save register
	;-------------------------------------
	push	ax
	push	bx
	push	si
	push	es
	push	bp

	;-------------------------------------
	; Get arguments
	;-------------------------------------
	mov	si,	[bp + 4]

	;-------------------------------------
	; Get font address
	;-------------------------------------
	mov	ax,	0x1130
	mov	bh,	0x06
	int	10h

	;-------------------------------------
	; Save font address
	;-------------------------------------
	mov	[si + 0],	es
	mov	[si + 2],	bp

	;-------------------------------------
	; Recover register
	;-------------------------------------
	pop	bp
	pop	es
	pop	si
	pop	bx
	pop	ax

	;-------------------------------------
	; Scrap stack frame
	;-------------------------------------
	mov	sp,	bp
	pop	bp

	ret
