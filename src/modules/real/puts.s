puts:
	;-------------------------------------
	; Build stack frame
	;-------------------------------------
	push	bp
	mov	bp,	sp

	;-------------------------------------
	; Save register
	;-------------------------------------
	push	ax
	push	bx
	push	si

	;-------------------------------------
	; Get arguments
	;-------------------------------------
	mov	si,	[bp + 4]		; SI = Adress to string

	;-------------------------------------
	; Start processing
	;-------------------------------------
	mov	ah,	0x0E
	mov	bx,	0x0000
	cld
.10L:

	lodsb

	cmp	al,	0
	je	.10E

	int	0x10
	jmp	.10L
.10E:

	;-------------------------------------
	; Recover register
	;-------------------------------------
	pop	si
	pop	bx
	pop	ax

	;-------------------------------------
	; Scrap stack frame
	;-------------------------------------
	mov	sp,	bp
	pop	bp

	ret
