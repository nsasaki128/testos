putc:
	;-------------------------------------
	; Build stack frame
	;-------------------------------------
	push	bp
	mov	bp,	sp

	;-------------------------------------
	; Save register
	;-------------------------------------
	push	ax
	push	bx

	;-------------------------------------
	; Start processing
	;-------------------------------------
	mov	al,	[bp + 4]
	mov	ah,	0x0E
	mov	bx,	0x0000
	int	0x10

	;-------------------------------------
	; Recover register
	;-------------------------------------
	pop	bx
	pop	ax

	;-------------------------------------
	; Scrap stack frame
	;-------------------------------------
	mov	sp,	bp
	pop	bp

	ret
